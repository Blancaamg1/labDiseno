package edu.esi.ds.esientradas.dto;

public class DtoCompraInfo {
    private long idEspectaculo;
    private String nombreEspectaculo;
    private String tipoMapa;
    private String nombreEscenario;
    private String modoSeleccion;
    private long idEscenario;

    
    public long getIdEspectaculo() {
        return idEspectaculo;
    }
    public void setIdEspectaculo(long idEspectaculo) {
        this.idEspectaculo = idEspectaculo;
    }
    public String getNombreEspectaculo() {
        return nombreEspectaculo;
    }
    public void setNombreEspectaculo(String nombreEspectaculo) {
        this.nombreEspectaculo = nombreEspectaculo;
    }
    public String getTipoMapa() {
        return tipoMapa;
    }
    public void setTipoMapa(String tipoMapa) {
        this.tipoMapa = tipoMapa;
    }
    public String getNombreEscenario() {
        return nombreEscenario;
    }
    public void setNombreEscenario(String nombreEscenario) {
        this.nombreEscenario = nombreEscenario;
    }
    public String getModoSeleccion() {
        return modoSeleccion;
    }
    public void setModoSeleccion(String modoSeleccion) {
        this.modoSeleccion = modoSeleccion;
    }
    public long getIdEscenario() {
        return idEscenario;
    }
    public void setIdEscenario(long idEscenario) {
        this.idEscenario = idEscenario;
    }

    
}
